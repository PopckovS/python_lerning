'''Можно использовать не текущую дату, а создать обьект с нужной нам датой временем.
У класса datetime есть конструктор datetime() в который и передадим параметры для создания обьекта.
Параметры (год, месяц, день, час, минута, секунда, микросекунда, часовой пояс)'''
print(start_block)
date = datetime.datetime(2020, 5, 20, 1, 1, 1)
