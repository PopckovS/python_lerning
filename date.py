'''Дата в Python, надо импортировать модуль с именем datetime'''
import datetime # Импортируем модуль для работы с датой временем

start_block = '==================='

# ========================================================
'''Показать текущую дату время, Дата содержит год, месяц, день, час, минуту, секунду и микросекунду.'''
print(start_block)
x = datetime.datetime.now()
print(x)
print(x.year)
print(x.strftime("%A"))
